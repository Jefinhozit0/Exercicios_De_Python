L = [5, 7, 2, 9, 4, 1, 3]

# Quantidade de elementos na lista
quantidade_elementos = len(L)
print(f"Quantidade de elementos: {quantidade_elementos}")

# Maior valor na lista
maior_valor = max(L)
print(f"Maior valor: {maior_valor}")

# Menor valor na lista
menor_valor = min(L)
print(f"Menor valor: {menor_valor}")

# Soma de todos os elementos
soma_elementos = sum(L)
print(f"Soma de todos os elementos: {soma_elementos}")

# Lista em ordem crescente
lista_crescente = sorted(L)
print(f"Lista em ordem crescente: {lista_crescente}")

# Lista em ordem decrescente
lista_decrescente = sorted(L, reverse=True)
print(f"Lista em ordem decrescente: {lista_decrescente}")